function levelingToValRatio(fulltext: string): { leveling: number | number[]; units: string; } {

  let s = fulltext.replace(/^\+/, '').replace(/[()]/g, '').split(/(%.*)/, 2);
  const leveling = s[0];

  let fail_count = 0;
  let units = s[1]?.trim() || '';

  if (fulltext === 'based on level') return { leveling: -1, units: leveling };
  if (leveling.trim() === "0.5 +  0.175") return { leveling: [0.5, 0.675], units: "with_infinity_edge" };
  if (fulltext === "+ Siphoning Strike stacks") return { leveling: [1], units: "Siphoning Strike stacks" };

  // splits "0 − 8.2" or "100 / 200 / 300" or "10 / 20 / 30 / 40 / 50"
  const arr = leveling.split(/[/−]/).map((x: string) => {
    let n = Number(x);
    if (Number.isFinite(n)) return n;
    // Split at the ' ' and tryif that works
    const split = x.trim().split(/ (.*)/);
    console.log(`[WARN] Stat leveling '${leveling}' needed to split value: '${x}' into: [${split}]`);

    n = Number(split[0]);
    if (Number.isFinite(n)) {
      units = units + split[1] || '';
      return n;
    };
    return fail_count++

  });
  if (fail_count > 0) {
    return { leveling: 0, units: leveling.trim() };
  }
  if (arr.length === 1) {
    return { leveling: arr[0], units: units };
  }
  return { leveling: arr, units: units };
}

function makeRatioObj(root: parenthesis.ArrayTree): SubRatio {
  const sub_ratios: SubRatio[] = [];
  let fulltext = "";
  let is_based_on_level = false;

  for (const [idx, ratio] of Object.entries(root)) {
    if (Array.isArray(ratio)) {
      let sub = makeRatioObj(ratio as parenthesis.ArrayTree);
      if (sub.units == 'based on level') {
        // set flag, do not add as sub_ratio
        // Ex. 50 − 305 (based on level) (+ 80% bonus AD)
        is_based_on_level = true;
      } else {
        sub_ratios.push(sub);
      }
    } else {
      fulltext = fulltext + ratio;
    }
  }
  //remove the () and + before.
  let s = fulltext.replace(/^\+/, '').replace(/[()]/g, '').split(/(%.*)/, 2);


  let vals = levelingToValRatio(fulltext);
  // console.log("levelingToValRatio", fulltext, "to", JSON.stringify(vals));
  let apply = s.length > 1 ? (vals.units === '' ? 'percent' : 'stat') : 'flat';

  // const per = stat_raw.includes('per');
  // const per_100 = stat_raw.includes('100');

  if (is_based_on_level) {
    apply = 'based_on_level';
    vals.leveling = numberExpandOnLevel(s[0]);
    // levelingToValRatio finds the worng stat.
    vals.units = '';
  }
  const user = apply === "flat" ? "none" : fulltext.indexOf("target") > -1 ? "target" : "player";

  return { values: vals.leveling, units: vals.units || '', stat: ratio_to_player_stat(vals.units), user, apply, fulltext, sub_ratios: sub_ratios.length === 0 ? undefined : sub_ratios } as SubRatio;

  // const apply = is_text ? 'text' : (per && per_100) ? 'per100' : per ? "per" : ispercent ? 'percent' : "flat";

  // return { values, user, stat, apply, stat_raw: fulltext, sub_ratios: sub_ratios.length === 0 ? undefined : sub_ratios };
}

function numberExpandOnLevel(values: string): number[] {
  const list: number[] = [];
  const found = values.split(' − ');
  const start = Number(found[0]);
  const end = Number(found[1]);
  const range = 18;
  const diff = (end - start) / (range - 1);
  for (let i = 0; i < range; i++) {
    const answer = +(start + diff * i)
    list.push(+answer.toFixed(2) || i);
  }
  return list;
}